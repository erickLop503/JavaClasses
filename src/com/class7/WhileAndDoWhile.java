package com.class7;

public class WhileAndDoWhile {

	public static void main(String[] args) {
		
		int a=10;
		
		do {
			System.out.println("Hello");
			a++;
		} while (a>15);
		
		//--------------------------------------------------
		int b=10;
		
		while (b>15) {
			System.out.println("Bye");
			b++;
		}
	}
}
